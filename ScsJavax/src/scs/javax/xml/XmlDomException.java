package scs.javax.xml;

public class XmlDomException extends Exception
{

  public XmlDomException ()
  {
    super();
  }

  public XmlDomException ( String message )
  {
    super( message );
  }

  public XmlDomException ( Throwable cause )
  {
    super( cause );
  }

  public XmlDomException ( String message, Throwable cause )
  {
    super( message, cause );
  }

}
